"""
Единый источник наборов символов для генерации данных, обучения и экспорта.
Вынесен в отдельный модуль без зависимостей (без PIL/torch), чтобы его можно
было импортировать в любом окружении — например, в CI-джобе export, где
Pillow намеренно не установлена.

Модель — не мультиязычная: под каждый язык обучается отдельная модель со
своим набором символов (свой CTC-classifier). Поэтому для каждого языка
храним отдельный CHARSET и отдельный чекпоинт/onnx-файл (см. train.py,
export_onnx.py). Значение CHARSET (без суффикса) оставлено для обратной
совместимости и равно английскому набору.
"""

# Общие для всех языков символы: пунктуация, цифры, латиница (часто
# встречается в текстах на любом языке — цифры, аббревиатуры и т.п.)
_COMMON = (
    " !\"#$%&'()*+,-./0123456789:;<=>?@"
    "ABCDEFGHIJKLMNOPQRSTUVWXYZ[\\]^_`"
    "abcdefghijklmnopqrstuvwxyz{|}~"
)

CHARSET_EN = _COMMON

# Кириллица (включая ё/Ё) поверх общего набора — модель для русского языка
# по-прежнему умеет распознавать цифры и латинские вставки.
CHARSET_RU = _COMMON + "АБВГДЕЁЖЗИЙКЛМНОПРСТУФХЦЧШЩЪЫЬЭЮЯабвгдеёжзийклмнопрстуфхцчшщъыьэюя"

CHARSETS = {
    "en": CHARSET_EN,
    "ru": CHARSET_RU,
}

# Оставлено для обратной совместимости с уже написанным кодом/импортами.
CHARSET = CHARSET_EN


def get_charset(lang: str = "en") -> str:
    """Возвращает набор символов для указанного языка ('en' или 'ru')."""
    try:
        return CHARSETS[lang]
    except KeyError:
        raise ValueError(
            f"Неизвестный язык '{lang}'. Доступные варианты: {', '.join(CHARSETS)}"
        )


def get_char_to_idx(lang: str = "en") -> dict:
    """Индекс 0 зарезервирован под CTC-blank, поэтому символы нумеруются с 1."""
    charset = get_charset(lang)
    return {c: i + 1 for i, c in enumerate(charset)}


# Оставлено для обратной совместимости.
CHAR_TO_IDX = get_char_to_idx("en")
